#argumentos y parametros

def restar(num1,num2):#cuando la funcion recibe los valores estos son llamados: Parametros
    return num1-num2

#argumentos por posicion
print(restar(4,3))#cuando se llama a la funcion y se le dan valores son llamados : Argumentos

#argumentos por nombre
print(restar(num2=4,num1=2))