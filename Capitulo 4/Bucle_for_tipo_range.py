#El range siempre llega hasta el n -1
for i in range(10): #Imprimira de 0 a 9
    print(i,end=".")

print("\n\n")

#Otro ejemplo ----------------------------------------------------------------------------------  

for i in range(5,11):#esto es un rango de 5 hasta 11 
    print(i,end=".")

print("\n\n")

#otro ejemplo -----------------------------------------------------------------------------------------

for i in range(2,21,2):#Aqui le decimos que empieze desde el 2 hasta el 20 y que vaya de 2 en 2
    print(i, end=",")
print("\n\n")

for i in range(20,2,-2):#Hace lo mismo que el de arriba solo que alrevez
    print(i, end=",")